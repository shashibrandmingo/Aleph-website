function initServiceMegaMenu() {
  const desktopContainer = document.getElementById('service-mega-menu');
  if (desktopContainer && !desktopContainer.dataset.loaded) {
    desktopContainer.innerHTML = window.serviceDropdownHTML;
    desktopContainer.dataset.loaded = 'true';
    setupDesktopMegaMenuSwitchers(desktopContainer);
  }

  const mobileContainer = document.getElementById('service-mobile-submenu');
  if (mobileContainer && !mobileContainer.dataset.loaded) {
    mobileContainer.innerHTML = window.serviceMobileDropdownHTML;
    mobileContainer.dataset.loaded = 'true';
    setupMobileNestedAccordions(mobileContainer);
  }
}

// Re-inject on mutations (e.g. PJAX swaps)
document.addEventListener('DOMContentLoaded', () => {
  const observer = new MutationObserver(() => {
    initServiceMegaMenu();
  });
  const mainContent = document.getElementById('main-content');
  if (mainContent) {
    observer.observe(mainContent, { childList: true, subtree: true });
  }
});

// Interactive tab switching logic for Desktop
function setupDesktopMegaMenuSwitchers(container) {
  const tabs = container.querySelectorAll('.mega-sidebar-tab');
  const cols = container.querySelectorAll('.mega-col');
  
  tabs.forEach(tab => {
    tab.addEventListener('mouseenter', () => {
      const targetColIndex = tab.getAttribute('data-col');
      
      // Update tabs state
      tabs.forEach(t => t.classList.remove('active'));
      tab.classList.add('active');
      
      // Update columns state
      cols.forEach(col => {
        col.classList.remove('active');
        if (col.id === 'mega-col-' + targetColIndex) {
          col.classList.add('active');
        }
      });
    });
  });
}

// Accordion switching logic for Mobile
function setupMobileNestedAccordions(container) {
  const nestedLinks = container.querySelectorAll('.mobile-nested-link');
  
  nestedLinks.forEach(link => {
    if (link.dataset.listenerAttached) return;
    link.dataset.listenerAttached = 'true';
    
    link.addEventListener('click', (e) => {
      e.preventDefault();
      e.stopPropagation();
      
      const submenu = link.nextElementSibling;
      const isOpen = link.classList.contains('open');
      
      // Close other submenus first
      nestedLinks.forEach(otherLink => {
        if (otherLink !== link) {
          otherLink.classList.remove('open');
          const otherSub = otherLink.nextElementSibling;
          if (otherSub) otherSub.classList.remove('open');
        }
      });
      
      if (isOpen) {
        link.classList.remove('open');
        if (submenu) submenu.classList.remove('open');
      } else {
        link.classList.add('open');
        if (submenu) submenu.classList.add('open');
      }
    });
  });
}
